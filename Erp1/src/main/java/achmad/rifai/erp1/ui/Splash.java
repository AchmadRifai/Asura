    private void jalan() throws Exception {
        if(nek!=null)nek.setVisible(false);
        Thread.sleep(300);
        prog.setValue(10);
        conn();
        check1();
        pindah();
    }

    private void conn() throws InterruptedException {
        java.io.File f=new java.io.File(System.getProperty("user.home")+"/.asura/work");
        if(!f.exists()){
            achmad.rifai.erp1.ui.dial.Conn c=new achmad.rifai.erp1.ui.dial.Conn(this, true);
            c.setVisible(true);
            while(c.isVisible()){}
        }Thread.sleep(300);
        prog.setValue(30);
    }

    private void check1() throws InterruptedException {
        java.io.File f1=new java.io.File(System.getProperty("user.home")+"/.asura/work/pri"),f2=
                new java.io.File(System.getProperty("user.home")+"/.asura/work/pub");
        if(!f1.exists()||!f2.exists()){
            if(f1.exists())f1.delete();
            if(f2.exists())f2.delete();
            achmad.rifai.erp1.ui.dial.Conn c=new achmad.rifai.erp1.ui.dial.Conn(this, true);
            c.setVisible(true);
            while(c.isVisible()){}
        }Thread.sleep(300);
        prog.setValue(70);
    }

    private void pindah() {
        prog.setValue(100);
        if(nek==null)System.exit(0);
        else nek.setVisible(true);
        this.setVisible(false);
    }
