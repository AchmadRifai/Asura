    private void sembunyi() throws Exception {
        Db d=achmad.rifai.erp1.util.Work.loadDB();
        jejakSembunyi(d);
        kakure(d);
        d.close();
    }

    private void jejakSembunyi(Db d) throws Exception {
        achmad.rifai.erp1.entity.dao.DAOTracks dao=new achmad.rifai.erp1.entity.dao.DAOTracks(d);
        achmad.rifai.erp1.entity.Tracks a=dao.current(k.getId()),b=dao.current(k.getId());
        java.util.List<achmad.rifai.erp1.entity.Jejak>l=a.getL();
        l.add(new achmad.rifai.erp1.entity.Jejak("Menyembunyikan pesan "+p.getKode(), k.getId()));
        b.setL(l);
        dao.update(a, b);
    }

    private void kakure(Db d) throws Exception {
        achmad.rifai.erp1.entity.Pesan a=achmad.rifai.erp1.entity.Pesan.of(d, p.getKode()),b=achmad.rifai.erp1.entity.Pesan.of(d, p.getKode());
        java.util.List<achmad.rifai.erp1.entity.Penerima>l=a.getKe();
        for(int x=0;x<l.size();x++){
            achmad.rifai.erp1.entity.Penerima pe=l.get(x);
            if(k.getId() == null ? pe.getAkun() == null : k.getId().equals(pe.getAkun())){
                l.remove(x);
                pe.setSembunyi(true);
                l.add(pe);
                break;
            }
        }b.setKe(l);
        new achmad.rifai.erp1.entity.dao.DAOPesan(d).update(a, b);
    }
