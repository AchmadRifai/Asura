    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int x=JOptionPane.showConfirmDialog(rootPane, "Apa anda ingin menghapus pesan "+p.getKode()+"?", "HAPUS?", JOptionPane.YES_NO_OPTION);
        if(x==JOptionPane.YES_OPTION){
            new Thread(()->{try {
                sembunyi();
                } catch (Exception ex) {
                    achmad.rifai.erp1.util.Db.hindar(ex);
                }setVisible(false);
                dispose();
            }).start();
            this.setCursor(new java.awt.Cursor(java.awt.Cursor.WAIT_CURSOR));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void sejajarkan() throws Exception {
        achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
        achmad.rifai.erp1.entity.Pesan a=achmad.rifai.erp1.entity.Pesan.of(d, p.getKode()),
                b=achmad.rifai.erp1.entity.Pesan.of(d, p.getKode());
        java.util.List<achmad.rifai.erp1.entity.Penerima>l=a.getKe();
        for(int x=0;x<l.size();x++){
            achmad.rifai.erp1.entity.Penerima pe=l.get(x);
            if(!pe.isTerbaca()&&(k.getId() == null ? pe.getAkun() == null : k.getId().equals(pe.getAkun()))){
                l.remove(x);
                pe.setTerbaca(true);
                l.add(pe);
                break;
            }
        }b.setKe(l);
        new achmad.rifai.erp1.entity.dao.DAOPesan(d).update(a, b);
        d.close();
    }

    private boolean terbaca() {
        boolean b=true;
        for(achmad.rifai.erp1.entity.Penerima pe:p.getKe())if(!pe.isTerbaca()&&(pe.getAkun() == null ? 
                k.getId() == null : pe.getAkun().equals(k.getId()))){
            b=false;
            break;
        }return b;
    }
