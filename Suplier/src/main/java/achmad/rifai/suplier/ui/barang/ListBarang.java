    private void next(Barang b,achmad.rifai.erp1.util.Db d) throws Exception {
        javax.swing.table.DefaultTableModel m=(javax.swing.table.DefaultTableModel) data.getModel();
        for(achmad.rifai.erp1.entity.Pembelian p:new achmad.rifai.erp1.entity.dao.DAOPembelian(d).all())
            for(achmad.rifai.erp1.entity.ItemBeli i:p.getItems())
                if(b.getKode() == null ? i.getBarang() == null : b.getKode().equals(i.getBarang())){
                    m.addRow(new Object[]{b.getNama(),""+b.getStok()+" "+b.getSatuan(),i.getHarga(),b.getHarga()});
                    return;
                }
        m.addRow(new Object[]{b.getNama(),""+b.getStok()+" "+b.getSatuan(),
            org.joda.money.Money.zero(CurrencyUnit.of("IDR")),b.getHarga()});
    }

    private void pengingat() {
        try {
            achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
            for(achmad.rifai.erp1.entity.Barang ba:new achmad.rifai.erp1.entity.dao.DAOBarang(d).all()){
                if(10>ba.getStok())next2(ba,d);
            }d.close();
        } catch (Exception ex) {
            achmad.rifai.erp1.util.Db.hindar(ex);
        }
    }

    private void next2(Barang ba, Db d) throws Exception {
        boolean oleh=achmad.rifai.suplier.util.Work.isSupportedMarket(ba,d);
        if(oleh||true)JOptionPane.showMessageDialog(rootPane, "Barang "+ba.getNama()+" tinggal sedikit");
    }
