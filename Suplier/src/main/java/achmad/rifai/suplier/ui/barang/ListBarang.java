    private void reload() {
        try {
            achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
            javax.swing.table.DefaultTableModel m=new javax.swing.table.DefaultTableModel(new String[]{"Merk","Stok","Harga Beli","Harga Jual"}, 0){
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }};data.setModel(m);
            for(Barang b:new achmad.rifai.erp1.entity.dao.DAOBarang(d).all())next(b,d);
            d.close();
            Thread.sleep(5000);
        } catch (Exception ex) {
            achmad.rifai.erp1.util.Db.hindar(ex);
        }
    }


