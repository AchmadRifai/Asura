    private void kodeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_kodeKeyReleased
        if(!kode.getText().isEmpty())try {
            achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
            achmad.rifai.erp1.entity.BulanBonus bu=achmad.rifai.erp1.entity.BulanBonus.of(kode.getText(), d);
            if(bu==null)kode.setForeground(Color.red);
            else kode.setForeground(Color.BLACK);
            d.close();
        } catch (Exception ex) {
            achmad.rifai.erp1.util.Db.hindar(ex);
        }refresh();
    }//GEN-LAST:event_kodeKeyReleased

    private void saving() {
        if(b==null){
            b=new achmad.rifai.erp1.entity.BulanBonus();
            b.setDeleted(false);
        }b.setBln(bln.getItemAt(bln.getSelectedIndex()));
        b.setKode(kode.getText());
        b.setPeg(peg.getItemAt(peg.getSelectedIndex()));
        b.setThn(java.time.Year.of((int) thn.getValue()));
        java.util.List<achmad.rifai.erp1.entity.Bonusan>l=new java.util.LinkedList<>();
        for(javax.swing.JInternalFrame i:lst.getAllFrames()){
            Bon bo=(Bon) i;
            l.add(bo.genData());
        }b.setL(l);
    }

    private void writeDB() {
    try {
        achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
        achmad.rifai.erp1.entity.BulanBonus bu=achmad.rifai.erp1.entity.BulanBonus.of(b.getKode(), d);
        achmad.rifai.erp1.entity.dao.DAOBulanBonus dao=new achmad.rifai.erp1.entity.dao.DAOBulanBonus(d);
        if(bu==null)dao.insert(b);
        else dao.update(bu, b);
        d.close();
    } catch (Exception ex) {
        achmad.rifai.erp1.util.Db.hindar(ex);
    }
    }
