    private void karyawane() {
    try {
        achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
        java.util.List<achmad.rifai.erp1.entity.Karyawan>l=new achmad.rifai.erp1.entity.dao.DAOKaryawan(d).all();
        l.forEach((k)->{peg.addItem(k.getId());});
        d.close();
    } catch (Exception ex) {
        achmad.rifai.erp1.util.Db.hindar(ex);
    }
    }

    private void inisial() {
        kode.setEnabled(false);
        kode.setText(b.getKode());
        bln.setSelectedItem(b.getBln());
        peg.setSelectedItem(b.getPeg());
        s.setEnabled(true);
        thn.setModel(new javax.swing.SpinnerNumberModel(1900+b.getThn().getValue(), 1900+Year.now().getValue(), 0, 1));
        allLST();
    }

    private void allLST() {
        b.getL().forEach((bo)->{
            java.awt.GridBagConstraints gbc=new java.awt.GridBagConstraints();
            gbc.gridx=x;
            gbc.gridy=y;
            Bon bon=new Bon();
            bon.setData(bo);
            bon.setVisible(true);
            lst.add(bon, gbc);
            x++;
            if(x==3){
                x=0;
                y++;
            }
        });
    }

    private void refresh() {
        s.setEnabled(!kode.getText().isEmpty()&&Color.BLACK==kode.getForeground());
    }
