    private void pelanggane() {
    try {
        achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
        achmad.rifai.erp1.entity.dao.DAOPelanggan dao=new achmad.rifai.erp1.entity.dao.DAOPelanggan(d);
        for(achmad.rifai.erp1.entity.Pelanggan p:dao.all())
            pelanggan.addItem(p.getKode());
        d.close();
    } catch (Exception ex) {
        achmad.rifai.erp1.util.Db.hindar(ex);
    }
    }

    private void tgle() {
        java.sql.Date d1=java.sql.Date.valueOf(LocalDate.now()),d2;
        if(p!=null){
            if(p.getTgl().before(java.sql.Date.valueOf(d1.toLocalDate().minusYears(3))))d2=p.getTgl();
            else d2=java.sql.Date.valueOf(d1.toLocalDate().minusYears(3));
        }else d2=java.sql.Date.valueOf(d1.toLocalDate().minusYears(3));
        while(!d1.before(d2)){
            tgl.addItem(d1);
            d1=java.sql.Date.valueOf(d1.toLocalDate().minusDays(1));
        }
    }

    private void inisial() {
        ket.setText(p.getKet());
        nota.setText(p.getNota());
        pelanggan.setSelectedItem(p.getPelanggan());
        tgl.setSelectedItem(p.getTgl());
        n.setEnabled(true);
    }

    private void refresh() {
        n.setEnabled(!nota.getText().isEmpty()&&Color.BLACK==nota.getForeground()&&!ket.getText().isEmpty());
    }

    private void saving() {
        if(p==null){
            p=new achmad.rifai.erp1.entity.Penjualan();
            p.setDeleted(false);
        }p.setKet(ket.getText());
        p.setNota(nota.getText());
        p.setPelanggan(pelanggan.getItemAt(pelanggan.getSelectedIndex()));
        p.setTgl(tgl.getItemAt(tgl.getSelectedIndex()));
    }
