    private void refresh() {
        s.setEnabled(!kode.getText().isEmpty()&&Color.BLACK==kode.getForeground()&&!ket.getText().isEmpty());
    }

    private void saving() {
        if(r==null){
            r=new achmad.rifai.erp1.entity.Rekening();
            r.setDeleted(false);
        }r.setGolongan(golongan.getItemAt(golongan.getSelectedIndex()));
        r.setKet(ket.getText());
        r.setKode(kode.getText());
        r.setPosisi(posisi.getItemAt(posisi.getSelectedIndex()));
        writeDB();
    }

    private void writeDB() {
    try {
        achmad.rifai.admin.util.Work.jejak(id, "Menyimpan perubahan aset "+r.getKode());
        achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
        achmad.rifai.erp1.entity.Rekening re=achmad.rifai.erp1.entity.Rekening.of(d, r.getKode());
        achmad.rifai.erp1.entity.dao.DAORekening dao=new achmad.rifai.erp1.entity.dao.DAORekening(d);
        if(re==null)dao.insert(r);
        else dao.update(re, r);
        d.close();
    } catch (Exception ex) {
        achmad.rifai.erp1.util.Db.hindar(ex);
    }
    }
