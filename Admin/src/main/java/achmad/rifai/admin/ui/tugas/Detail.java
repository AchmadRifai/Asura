    private void petugase() {
        javax.swing.table.DefaultTableModel m=(javax.swing.table.DefaultTableModel) petugas.getModel();
        t.getL().forEach((p)->{m.addRow(new Object[]{p.getKaryawan(),p.isDiambil(),p.isSedang(),p.isTerlaksana()});});
    }

    private void status() {
        if(t.isBatal())batal.setText("Dibatalkan");
        else batal.setText("Tidak batal");
        if(t.isPending())pending.setText("Ditunda sementara");
        else pending.setText("Dikerjakan");
    }
