    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int x=JOptionPane.showConfirmDialog(rootPane, "Apa anda ingin menghapus suplier ini?", "Hapus?", JOptionPane.YES_NO_OPTION);
        if(x==JOptionPane.YES_OPTION)try {
            achmad.rifai.admin.util.Work.jejak(id, "Menghapus Suplier "+s.getKode());
            achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
            new achmad.rifai.erp1.entity.dao.DAOSuplier(d).delete(s);
            d.close();
            this.setVisible(false);
        } catch (Exception ex) {
            achmad.rifai.erp1.util.Db.hindar(ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

