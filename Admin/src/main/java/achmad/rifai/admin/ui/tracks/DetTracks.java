    private void refresh(List<Jejak> l) {
        javax.swing.table.DefaultTableModel m=new javax.swing.table.DefaultTableModel(new String[]{"Kegiatan","Waktu"}, 0){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };isi.setModel(m);
        l.forEach((j)->m.addRow(new Object[]{j.getAksi(),j.getWaktu()}));
    }
