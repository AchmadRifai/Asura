    private void refresh() {
        n.setEnabled(!kode.getText().isEmpty()&&!ket.getText().isEmpty()&&Color.BLACK==kode.getForeground());
    }

    private void saving() {
        if(t==null){
            t=new achmad.rifai.erp1.entity.Tugas();
            t.setDeleted(false);
            t.setL(new java.util.LinkedList<>());
        }t.setBatal(batal.isSelected());
        t.setKet(ket.getText());
        t.setKode(kode.getText());
        t.setNo(Integer.parseInt(""+no.getValue()));
        t.setPending(pending.isSelected());
        t.setTgl((Date) tgl.getValue());
    }
