    private void loadAll() {
        loadKaryawan();
        t.getL().forEach((p)->{filling(p);});
        this.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
        s.setEnabled(true);
    }

    private void loadKaryawan() {
    try {
        achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
        java.util.List<achmad.rifai.erp1.entity.Karyawan>l=new achmad.rifai.erp1.entity.dao.DAOKaryawan(d).all();
        int x = 0,y = 0;
        for(achmad.rifai.erp1.entity.Karyawan k:l){
            Pet p=new Pet();
            p.setKaryawan(k.getId());
            java.awt.GridBagConstraints gbc=new java.awt.GridBagConstraints();
            gbc.gridx=x;
            gbc.gridy=y;
            lst.add(p, gbc);
            x++;
            if(x==3){
                x=0;
                y++;
            }
        }d.close();
    } catch (Exception ex) {
        achmad.rifai.erp1.util.Db.hindar(ex);
    }
    }

    private void filling(Petugas p) {
        for(javax.swing.JInternalFrame i:lst.getAllFrames()){
            Pet pe=(Pet) i;
            if(p.getKaryawan() == null ? pe.getKaryawan() == null : p.getKaryawan().equals(pe.getKaryawan())){
                pe.setAmbil(p.isDiambil());
                pe.setSedang(p.isSedang());
                pe.setTerlaksana(p.isTerlaksana());
                break;
            }
        }
    }

    private void writeDB() {
    try {
        achmad.rifai.admin.util.Work.jejak(id, "Mendata Tugas "+t.getKode());
        achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
        achmad.rifai.erp1.entity.Tugas tu=achmad.rifai.erp1.entity.Tugas.of(d, t.getKode());
        achmad.rifai.erp1.entity.dao.DAOTugas dao=new achmad.rifai.erp1.entity.dao.DAOTugas(d);
        if(tu==null)dao.insert(t);
        else dao.update(tu, t);
        d.close();
    } catch (Exception ex) {
        achmad.rifai.erp1.util.Db.hindar(ex);
    }
    }
