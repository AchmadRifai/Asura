    private void saving() {
        java.util.List<Petugas>l=new java.util.LinkedList<>();
        for(javax.swing.JInternalFrame i:lst.getAllFrames()){
            Pet p=(Pet) i;
            if(p.isAmbil())l.add(p.genPetugas());
        }t.setL(l);
    }
