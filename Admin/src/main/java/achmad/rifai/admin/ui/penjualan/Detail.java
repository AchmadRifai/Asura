    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        this.setTitle("Detail Penjualan "+p.getNota());
        pelanggan.setText("Pelanggan : "+p.getPelanggan());
        tgl.setText("Tanggal : "+p.getTgl());
        total.setText("Total : "+p.getTotal());
        bayar.setText("Bayar : "+p.getTerbayar());
        kembali.setText("Kembali : "+p.getTerbayar().minus(p.getTotal()));
        items.removeAll();
        javax.swing.table.DefaultTableModel m=(javax.swing.table.DefaultTableModel) items.getModel();
        p.getItems().forEach((i)->{m.addRow(new Object[]{i.getBarang(),i.getJumlah(),i.getUang()});});
    }//GEN-LAST:event_formWindowOpened

