    private void barangItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_barangItemStateChanged
        if(0<barang.getSelectedIndex())try {
            achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
            achmad.rifai.erp1.entity.Barang b=achmad.rifai.erp1.entity.Barang.of(d, barang.getItemAt(barang.getSelectedIndex()));
            satuan.setText(b.getSatuan());
            d.close();
        } catch (Exception ex) {
            achmad.rifai.erp1.util.Db.hindar(ex);
        }
    }//GEN-LAST:event_barangItemStateChanged


