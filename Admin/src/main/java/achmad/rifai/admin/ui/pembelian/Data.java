    public achmad.rifai.erp1.entity.ItemBeli getData(){
        achmad.rifai.erp1.entity.ItemBeli i=new achmad.rifai.erp1.entity.ItemBeli();
        i.setSatuan(satuan.getText());
        i.setBarang(barang.getItemAt(barang.getSelectedIndex()));
        i.setHarga(org.joda.money.Money.of(CurrencyUnit.of("IDR"), Long.parseLong(harga.getText())));
        i.setJumlah(Integer.parseInt(""+jumlah.getValue()));
        return i;
    }

