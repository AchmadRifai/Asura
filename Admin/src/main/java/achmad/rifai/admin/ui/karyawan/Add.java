    private void idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idKeyReleased
    if(!id.getText().isEmpty())try {
        achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
        achmad.rifai.erp1.entity.Karyawan k=achmad.rifai.erp1.entity.Karyawan.of(d, id.getText());
        if(k!=null)id.setForeground(Color.red);
        else id.setForeground(Color.BLACK);
        d.close();
    } catch (Exception ex) {
        achmad.rifai.erp1.util.Db.hindar(ex);
    }refresh();
    if(!id.getText().isEmpty())k.setId(id.getText());
    else k.setId(null);
    }//GEN-LAST:event_idKeyReleased

