    private void jabatane() throws Exception {
        achmad.rifai.admin.util.Work.jejak(kode, "mulai mendata Karyawan");
        achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
        achmad.rifai.erp1.entity.dao.DAOJabatan dao=new achmad.rifai.erp1.entity.dao.DAOJabatan(d);
        for(achmad.rifai.erp1.entity.Jabatan j:dao.all())
            jabatan.addItem(j.getNama());
        d.close();
    }

    private void tayang() {
        blocked.setSelected(k.isBlocked());
        deleted.setSelected(k.isDeleted());if(null!=k.getEmail())email.setText(k.getEmail());
        else{
            bersih();
            return;
        }if(null!=k.getTelp())hp.setText(k.getTelp());
        else{
            bersih();
            return;
        }if(null!=k.getId()){
            id.setText(k.getId());
            id.setEnabled(false);
        }else{
            bersih();
            return;
        }if(k.getJabatan()!=null){
            for(int x=0;x<jabatan.getItemCount();x++){
                if(k.getJabatan() == null ? jabatan.getItemAt(x) == null : k.getJabatan().equals(jabatan.getItemAt(x))){
                    jabatan.setSelectedIndex(x);
                    break;
                }
            }
        }else{
            bersih();
            return;
        }if(k.getNama()!=null)nama.setText(k.getNama());
        else{
            bersih();
            return;
        }masuk.setSelected(k.isMasuk());
        if(null!=k.getHiredate())tgl.setText(""+k.getHiredate());
        else tgl.setText("");
    }

    private void refresh() {
        n.setEnabled(!email.getText().isEmpty()&&!hp.getText().isEmpty()&&Color.BLACK==id.getForeground()&&0<jabatan.getSelectedIndex()&&
        !nama.getText().isEmpty()&&achmad.rifai.admin.util.TglModel.isValidDate(tgl.getText()));
    }

@SuppressWarnings("null")
    private void simpan() {
    try {
        achmad.rifai.admin.util.Work.jejak(kode, "menyimpan perubahan data karyawan "+k.getId());
        achmad.rifai.erp1.util.Db d=achmad.rifai.erp1.util.Work.loadDB();
        achmad.rifai.erp1.entity.Karyawan a=achmad.rifai.erp1.entity.Karyawan.of(d, k.getId());
        if(a!=null){
            if(k.getAlamat()==null)k.setAlamat(a.getAlamat());
            if(k.getEmail()==null)k.setEmail(a.getEmail());
            if(k.getHiredate()==null)k.setHiredate(a.getHiredate());
            if(k.getId()==null)k.setId(a.getId());
            if(k.getJabatan()==null)k.setJabatan(a.getJabatan());
            if(k.getNama()==null)k.setNama(a.getNama());
            if(k.getPass()==null)k.setPass(a.getPass());
            if(k.getTelp()==null)k.setTelp(a.getTelp());
            new achmad.rifai.erp1.entity.dao.DAOKaryawan(d).update(a, k);
        }d.close();
    } catch (Exception ex) {
        achmad.rifai.erp1.util.Db.hindar(ex);
    }
    }

    private void bersih() {
        blocked.setSelected(false);
        deleted.setSelected(false);
        email.setText("");
        hp.setText("0");
        tgl.setText("");
        id.setText("");
        id.setEnabled(true);
        jabatan.setSelectedIndex(0);
        nama.setText("");
    }
